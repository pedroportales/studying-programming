// Aula sobre variáveis
#include <stdio.h>

int main() {
  // Declarando variáveis
  int idade; // Inteiro
  
  // Função para imprimir algo no console
  printf("Qual é a sua idade? ");
  scanf("%d", &idade); // Recebendo dados do usuário
  printf("A sua idade é %d\n", idade); // Imprimindo dado recebido
  
  return 0;
}
